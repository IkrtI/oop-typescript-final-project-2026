/**
 * ═══════════════════════════════════════════════════════════════════════
 * 📘 OOP Concept: Abstract Class (คลาสนามธรรม)
 * ═══════════════════════════════════════════════════════════════════════
 *
 * ลองจินตนาการว่า "BaseEntity" เหมือนกับ "แม่พิมพ์พื้นฐาน" ของของเล่น
 * ทุกครั้งที่เราสร้างของเล่นใหม่ ไม่ว่าจะเป็นรถ หรือ ตุ๊กตา
 * ของเล่นทุกชิ้นต้องมี:
 *   - รหัสประจำตัว (id)        → เปรียบเหมือนเลขบาร์โค้ดบนกล่อง
 *   - วันที่ผลิต (createdAt)    → เปรียบเหมือนวันที่พิมพ์บนกล่อง
 *   - วันที่แก้ไข (updatedAt)   → เปรียบเหมือนวันที่ตรวจสอบคุณภาพล่าสุด
 *
 * 🔑 Keyword: "abstract" หมายความว่า คลาสนี้ไม่สามารถสร้าง Object โดยตรงได้
 *    ต้องมีคลาสลูก (เช่น Product, Order) มาสืบทอด (extends) ไปใช้
 *
 * 🎯 ทำไมต้องใช้ Abstract Class?
 *    → เพื่อบังคับให้ทุก Model มี field พื้นฐานเหมือนกัน (Single Source of Truth)
 *    → ป้องกันการลืมใส่ id หรือ timestamp ในบาง Model
 *

 * ═══════════════════════════════════════════════════════════════════════
 */

// ─────────────────────────────────────────────────────────────────────
// 📌 TODO [bouquetofroses-01]: สร้าง Abstract Class ชื่อ BaseEntity
// ─────────────────────────────────────────────────────────────────────
// 💡 TypeScript Concept: "abstract class" คือ class ที่ใช้เป็นแม่แบบ
//    ไม่สามารถ new BaseEntity() ได้โดยตรง
//    แต่ class อื่นสามารถ extends BaseEntity เพื่อรับ field เหล่านี้ไป
//
// 📖 Syntax:
//   export abstract class ClassName {
//     propertyName: type;
//   }
//
// 🎯 ต้องมี 3 properties:
//   1. id: string           → รหัสเฉพาะ (UUID) สำหรับระบุข้อมูลแต่ละ row
//   2. createdAt: string    → เวลาสร้าง ในรูปแบบ ISO 8601 (เช่น "2025-01-15T10:30:00.000Z")
//   3. updatedAt: string    → เวลาแก้ไขล่าสุด ในรูปแบบ ISO 8601
//
// ⬇️ เขียนโค้ดของคุณด้านล่าง ⬇️

export abstract class BaseEntity {
  id!: string;
  createdAt!: string;
  updatedAt!: string;
}
