/**
 * ═══════════════════════════════════════════════════════════════════════
 * 📘 TypeScript Concept: Enum สำหรับสถานะสินค้า
 * ═══════════════════════════════════════════════════════════════════════
 *
 * สถานะสินค้าบอกว่าสินค้าชิ้นนั้น "พร้อมขาย" หรือไม่
 * เหมือนป้ายที่ติดบนชั้นวางสินค้าในร้าน:
 *   🟢 ACTIVE       → "พร้อมขาย" — ลูกค้าสั่งซื้อได้
 *   🟡 OUT_OF_STOCK → "หมดชั่วคราว" — รอเติมสต็อก
 *   🔴 DISCONTINUED → "เลิกขาย" — ไม่รับออเดอร์อีกแล้ว
 *
 * ═══════════════════════════════════════════════════════════════════════
 */

export enum ProductStatus {
  ACTIVE = 'ACTIVE',
  OUT_OF_STOCK = 'OUT_OF_STOCK',
  DISCONTINUED = 'DISCONTINUED',
}
