/**
 * ═══════════════════════════════════════════════════════════════════════
 * 📘 TypeScript Concept: Enum (การจำกัดค่าที่เป็นไปได้)
 * ═══════════════════════════════════════════════════════════════════════
 *
 * ลองจินตนาการว่าคุณเปิดร้านค้า และสินค้ามีหลายหมวดหมู่
 * แต่คุณไม่อยากให้พนักงานพิมพ์หมวดหมู่อิสระ (เช่น "อิเลคโทรนิก" พิมพ์ผิด)
 * → Enum บังคับให้เลือกได้เฉพาะค่าที่กำหนดไว้เท่านั้น
 *
 * 🔑 Enum = "ตัวเลือกที่ถูกต้อง" เหมือนเมนู dropdown
 *    → ป้องกันการพิมพ์ผิด
 *    → TypeScript จะช่วยตรวจสอบให้ตอน compile
 *
 * ═══════════════════════════════════════════════════════════════════════
 */

// ─────────────────────────────────────────────────────────────────────
// 📌 TODO [Lukazx15-01]: กำหนดค่า Enum สำหรับหมวดหมู่สินค้า
// ─────────────────────────────────────────────────────────────────────
// 💡 Syntax: ENUM_KEY = 'ENUM_VALUE'
//    KEY คือชื่อที่ใช้ในโค้ด, VALUE คือค่าจริงที่เก็บในฐานข้อมูล
//
// 🎯 ต้องมี 5 หมวดหมู่ (ดูจาก docs/data-model.md):
//    ELECTRONICS  = 'ELECTRONICS'   → อิเล็กทรอนิกส์
//    CLOTHING     = 'CLOTHING'      → เสื้อผ้า
//    HOME         = 'HOME'          → ของใช้ในบ้าน
//    BEAUTY       = 'BEAUTY'        → ความงาม
//    OTHER        = 'OTHER'         → อื่นๆ
//
// ⬇️ เติมค่า Enum ด้านล่าง ⬇️

export enum ProductCategory {
  ELECTRONICS = 'ELECTRONICS',
  CLOTHING = 'CLOTHING',
  HOME = 'HOME',
  BEAUTY = 'BEAUTY',
  OTHER = 'OTHER',
}
