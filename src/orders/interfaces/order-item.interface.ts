/**
 * ═══════════════════════════════════════════════════════════════════════
 * 📘 TypeScript Concept: Interface (อินเทอร์เฟซ)
 * ═══════════════════════════════════════════════════════════════════════
 *
 * Interface กำหนด "รูปทรง" ของข้อมูล — บอกว่า Object ต้องมี field อะไรบ้าง
 *
 * OrderItem คือ "รายการสินค้าในออเดอร์"
 * เปรียบเสมือนแต่ละบรรทัดในใบเสร็จ:
 *   - สินค้าอะไร (productId, productName)
 *   - ราคาตอนซื้อ (priceAtPurchase) ← สำคัญ! เก็บราคา ณ ตอนนั้น
 *   - จำนวน (quantity)
 *   - ราคารวม (subtotal = price × quantity)
 *
 * 🔑 ทำไมต้องเก็บ "ราคาตอนซื้อ" (Price Snapshot)?
 *   → เพราะราคาสินค้าอาจเปลี่ยนในอนาคต
 *   → แต่ออเดอร์ต้องจำราคาตอนที่ลูกค้าสั่ง ไม่ใช่ราคาปัจจุบัน
 *   → เหมือนใบเสร็จ: พิมพ์ราคาไว้แล้ว ของขึ้นราคาทีหลังก็ไม่เปลี่ยน
 *
 * 👤 Assigned to: pockypycok (ณัชชา)
 * ═══════════════════════════════════════════════════════════════════════
 */

export interface OrderItem {
  /** อ้างอิง Product ID */
  productId: string;

  /** Snapshot ชื่อสินค้า ณ เวลาสั่ง */
  productName: string;

  /** Snapshot ราคา ณ เวลาสั่ง (ห้ามใช้ราคาปัจจุบัน!) */
  priceAtPurchase: number;

  /** จำนวนที่สั่ง */
  quantity: number;

  /** ราคารวมของรายการนี้ (priceAtPurchase × quantity) */
  subtotal: number;
}
